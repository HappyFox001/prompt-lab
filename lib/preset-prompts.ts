/**
 * 根据分类获取预设提示词
 */
export function getPresetsByCategory(category: string): PromptTestItem[] {
  return PRESET_PROMPTS.filter(p => p.category === category);
}

/**
 * 获取所有分类
 */
export function getCategories(): string[] {
  return Array.from(new Set(PRESET_PROMPTS.map(p => p.category || '其他')));
}
